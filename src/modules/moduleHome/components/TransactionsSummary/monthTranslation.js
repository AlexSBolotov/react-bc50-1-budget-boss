export const monthTranslation = name => {
  switch (name) {
    case 'Январь':
      return 'January';
    case 'Февраль':
      return 'February';
    case 'Март':
      return 'March';
    case 'Апрель':
      return 'April';
    case 'Май':
      return 'May';
    case 'Июнь':
      return 'June';
    case 'Июль':
      return 'July';
    case 'Август':
      return 'August';
    case 'Сентябрь':
      return 'September';
    case 'Октябрь':
      return 'October';
    case 'Ноябрь':
      return 'November';
    case 'Декабрь':
      return 'December';
    default:
      break;
  }
};

